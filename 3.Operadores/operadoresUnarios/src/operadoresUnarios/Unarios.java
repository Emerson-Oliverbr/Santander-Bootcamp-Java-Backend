package operadoresUnarios;

public class Unarios {

	public static void main(String[] args) {
		
		int numero = 5;
		numero++;
		
		System.out.println(numero);
		
		numero--;
		System.out.println(numero);
		
		int numero2 = 10;
		numero2 = -numero2;
		
		System.out.println(numero2);
		
		numero2 = numero2 * -1;
		System.out.println(numero2);
		
	}

}
